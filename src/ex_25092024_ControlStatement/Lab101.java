package ex_25092024_ControlStatement;

public class Lab101 {
    public static void main(String[] args) {
// even odd
        for (int i = 0; i < 51; i++) {
            if (i % 2 == 0) {
                System.out.println("Welcome " + i + "." + " You are a Even number");
            } else {
                System.out.println("Welcome " + i + "." + " You are a Odd number");
            }

        }

    }
}
