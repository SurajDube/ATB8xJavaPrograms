package ex_25092024_ControlStatement;

public class Lab096 {
    public static void main(String[] args) {
        for (int i = 0; i>0; i--)  {
            System.out.println("hello");

        }
        for (int i = 0; i>0; i++)  {
            System.out.println("hello");

        }

        for (int i = 0; i>0; --i)  {
            System.out.println("hello");

        }
        for (int i = 0; i>0; ++i)  {
            System.out.println("hello");

        }
    }
}
